public class question8 {
    public static void main(String[] args) {

        int a = 6;
        int b = 4;

        System.out.println("a & b  = " + (a & b));   // 4
        System.out.println("a | b  = " + (a | b));   // 6
        System.out.println("a ^ b  = " + (a ^ b));   // 2
        System.out.println("a << 1 = " + (a << 1));  // 12
        System.out.println("a >> 1 = " + (a >> 1));  // 3
    }
}